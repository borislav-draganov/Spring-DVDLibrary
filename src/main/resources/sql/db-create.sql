CREATE TABLE RATING (
  ID INT NOT NULL GENERATED BY DEFAULT AS IDENTITY(START WITH 1),
  NAME VARCHAR(45) NOT NULL,
  PRIMARY KEY (ID));

CREATE TABLE GENRE (
  ID INT NOT NULL GENERATED BY DEFAULT AS IDENTITY(START WITH 1),
  NAME VARCHAR(45) NOT NULL,
  PRIMARY KEY (ID));

CREATE TABLE LANGUAGE (
  ID INT NOT NULL GENERATED BY DEFAULT AS IDENTITY(START WITH 1),
  NAME VARCHAR(45) NOT NULL,
  PRIMARY KEY (ID));

CREATE TABLE AUDIO (
  ID INT NOT NULL GENERATED BY DEFAULT AS IDENTITY(START WITH 1),
  NAME VARCHAR(45) NOT NULL,
  PRIMARY KEY (ID));

CREATE TABLE MOVIE (
  ID INT NOT NULL GENERATED BY DEFAULT AS IDENTITY(START WITH 1),
  TITLE VARCHAR(45) NOT NULL,
  LENGTH INT NOT NULL,
  REALEASEDATE DATE,
  IMDB INT,
  RATING INT,
PRIMARY KEY (ID),
CONSTRAINT FK_RATING FOREIGN KEY (RATING) REFERENCES RATING(ID));