CREATE TABLE RATING (
  ID INT NOT NULL GENERATED BY DEFAULT AS IDENTITY(START WITH 1),
  NAME VARCHAR(45) NOT NULL,
  PRIMARY KEY (ID));

CREATE TABLE GENRE (
  ID INT NOT NULL GENERATED BY DEFAULT AS IDENTITY(START WITH 1),
  NAME VARCHAR(45) NOT NULL,
  PRIMARY KEY (ID));

CREATE TABLE LANGUAGE (
  ID INT NOT NULL GENERATED BY DEFAULT AS IDENTITY(START WITH 1),
  NAME VARCHAR(45) NOT NULL,
  PRIMARY KEY (ID));

CREATE TABLE AUDIO (
  ID INT NOT NULL GENERATED BY DEFAULT AS IDENTITY(START WITH 1),
  NAME VARCHAR(45) NOT NULL,
  PRIMARY KEY (ID));

CREATE TABLE MOVIE (
  ID INT NOT NULL GENERATED BY DEFAULT AS IDENTITY(START WITH 1),
  TITLE VARCHAR(45) NOT NULL,
  LENGTH INT NOT NULL,
  REALEASEDATE DATE,
  IMDB VARCHAR(45),
  RATING INT,
PRIMARY KEY (ID),
CONSTRAINT FK_RATING FOREIGN KEY (RATING) REFERENCES RATING(ID));

CREATE TABLE DVD (
  ID INT NOT NULL GENERATED BY DEFAULT AS IDENTITY(START WITH 1),
  MOVIE_ID INT NOT NULL,
  ISBN VARCHAR(45) NOT NULL,
  EDITION VARCHAR(45) NULL,
  SCREEN_FORMAT VARCHAR(10) NULL,
  REGION VARCHAR(45) NULL,
PRIMARY KEY (ID),
CONSTRAINT FK_MOVIE FOREIGN KEY (MOVIE_ID) REFERENCES MOVIE(ID));

CREATE TABLE MOVIE_GENRE (
  MOVIE_ID INT,
  GENRE_ID INT,
  CONSTRAINT FK_MG_MOVIE FOREIGN KEY (MOVIE_ID) REFERENCES MOVIE(ID),
  CONSTRAINT FK_MG_GENRE FOREIGN KEY (GENRE_ID) REFERENCES GENRE(ID));

CREATE TABLE DVD_AUDIO (
  DVD_ID INT,
  AUDIO_ID INT,
CONSTRAINT FK_DA_DVD FOREIGN KEY (DVD_ID) REFERENCES DVD(ID),
CONSTRAINT FK_DA_AUDIO FOREIGN KEY (AUDIO_ID) REFERENCES AUDIO(ID));

CREATE TABLE DVD_LANGUAGE (
  DVD_ID INT,
  LANGUAGE_ID INT,
  CONSTRAINT FK_DL_DVD FOREIGN KEY (DVD_ID) REFERENCES DVD(ID),
  CONSTRAINT FK_DL_LANGUAGE FOREIGN KEY (LANGUAGE_ID) REFERENCES LANGUAGE(ID));